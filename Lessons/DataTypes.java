package Lessons;
public class DataTypes {
      public static void main(String[] args) {
         
        // INTEGRAL DATA TYPE..
          byte num1 = 20;              // -128 to 127   
          int num2 = 300;              // -2,147,483,648  to  2,147,483,647
          short num3 = 30000;          //  -32,768 to 32,767
          long num4 =  43545234534L;  //	-9,223,372,036,854,775,808 to 9,223,372,036,854,775,807

          //FLOATING DATA TYPE..
          float num5 =  34.544F;      // can able to store 6 decimal point numbers
          double num6 = 48.987893245482345 ;    //  Can able to store upto 16 decimal point numbers

          //  CHARACTER DATA TYPE..
          char a = 'H';                        // store anything except number..

          boolean rightToVote = true;         //true or false 

      }   
}
