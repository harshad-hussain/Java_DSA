package Lessons;
public class Relational {
     public static void main(String[] args) {
          //Equality Opertors (==,!=);
          //Relational operators (<,<=,>=,>)   - Both are boolean  

          int a =10;
          int b =16;

          System.out.println(a==b);
          System.out.println(a != b);
          System.out.println(a > b);
          System.out.println( a >= b);
          System.out.println(a < b);
          System.out.println(a <= b);
          System.out.println(a != b);
     }
}
